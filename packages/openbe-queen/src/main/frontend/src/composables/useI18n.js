const currentLang = ref(localStorage.getItem('openbe-lang') || 'zh')

function t(key) {
  return (I18N[currentLang.value] && I18N[currentLang.value][key]) || key
}

function setLang(lang) {
  currentLang.value = lang
  localStorage.setItem('openbe-lang', lang)
}

function toggleLang() {
  setLang(currentLang.value === 'zh' ? 'en' : 'zh')
}

export function useI18n() {
  const nextLangLabel = computed(() => currentLang.value === 'zh' ? 'EN' : '中文')
  return { t, currentLang, setLang, toggleLang, nextLangLabel, I18N }
}
